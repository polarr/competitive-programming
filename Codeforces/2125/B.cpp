void solve(){
    ll a, b, k; cin >> a >> b >> k;
    ll g = gcd(a, b);

    ll aa = a/g, bb = b/g;
    if (aa <= k && bb <= k){
        cout << 1 << endl;
        return;
    }

    cout << 2 << endl;
    return;
}

int main(){
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    int tc;
    cin >> tc;
    for (int t = 1; t <= tc; ++t){
        // cout << "Case #" << t << ": ";
        solve();
    }
    return 0;
}