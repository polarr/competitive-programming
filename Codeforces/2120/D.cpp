const int MAX_N = 1e5 + 1;
const ll MOD = 1e9 + 7;

/** 
 * ALGORITHM: Modular Exponentiation 
 * PURPOSE: Computes x^n (mod m)
 * CONSTRAINT: m * m <= LLONG_MAX to prevent overflow
 * TIME: O(log n)
*/
ll mod_exp(ll x, ll n, ll m) {
	x %= m;

	ll res = 1;
	while (n > 0) {
		if (n % 2 == 1) { res = res * x % m; }
		x = x * x % m;
		n /= 2;
	}

	return res;
}

void solve(){
    ll a, b, k; cin >> a >> b >> k;

    ll n = k * (a - 1) + 1;
    ll m = k * (b - 1) % MOD;

    for (ll i = 1; i <= a; i++){
        m *= (n - i + 1) % MOD;
        m %= MOD;
        m *= mod_exp(i, MOD - 2, MOD);
        m %= MOD;
    }

    m++;
    m %= MOD;

    cout << n % MOD << " " << m << endl;
}

int main(){
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    int tc;
    cin >> tc;
    for (int t = 1; t <= tc; ++t){
        // cout << "Case #" << t << ": ";
        solve();
    }
    return 0;
}